def maximumDifference(nums):
    
    lst = []
    l= 0
    r = 1
    while r < len(nums):
        if sorted(nums, reverse=True) == nums:
            return -1
        if nums[l] < nums[r]:
            res = nums[r]-nums[l]
            lst.append(res)
        l += 1
            
    return max(lst) 

